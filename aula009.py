def soma(n1,n2) : return n1+n2
def minus(n1,n2) : return n1-n2
def multiply(n1,n2) : return n1*n2
def power(n1,n2) : return n1**n2
def division(n1,n2) : return n1/n2
def full_division(n1,n2) : return n1//n2

print(soma(10,2))
print(minus(10,2))
print(multiply(10,2))
print(power(10,2))
print(division(10,2))
print(full_division(10,2))