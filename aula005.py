# tipo de dado bool(boolean)
# Ao questionar algo em um programa, só existem duas possíveis respostas:
# Sim(true) e Não(false)
# Operações booleanas podem ser executadas utilizando diferentes operadores lógicos, dentre eles o ==. 
# Esse operador lógico checa de X valor é igual a Y valor, por exemplo:

print(1 == 1)

# A expressão acima retorna o valor "true", por que 1 é igual a 1

print('1' == 1)

# A expressão acima retorna "false", pois '1' não é igual a 1. uma string não é igual a um int