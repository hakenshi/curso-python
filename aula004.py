# Tips int e float
# int são números inteiros, ou seja, 1,2,3,4,5...
# variáveis do tipo int também podem ser negativas, um int sem um sinal(1), é considerado positivo

# print(11, type(11))
# print(-11, type(-11))
# print(0, type(0))

# floats são números de ponto flutuante
# o tipo float pode representar qualquer número seja ele positivo ou negativo, com vírgula ou sem vírgula.
# números de ponto flutuante são um meio que computador achou para converter números binários em números com vírgula, portanto, 0.1 + 0.2 é igual a 0.30000000000000004.

# print(0.1,type(0.1))
# print(-0.1,type(-0.1))
# print(0.1+0.2,type(0.1+0.2))


#a função type() é o equivalente de typeOf() do javascript, qualquer coisa dentro dos parênteses será exibido o tipo daquele valor.
print(type(1))
print(type(-1))
print(type('a'))
